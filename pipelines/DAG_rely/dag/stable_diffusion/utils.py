from diffusers.utils.torch_utils import randn_tensor
def get_latents(pipe, prompt, seed, n, device):
    device = torch.device(device)
    dtype=pipe.text_encoder.dtype
    
    if isinstance(prompt, str):
        batch_size = 1
    elif isinstance(prompt, list):
        batch_size = len(prompt)
        
    if type(seed) is not list:
        seed=[seed] * batch_size
        
    generators=[torch.Generator(device).manual_seed(int(s)) for s in seed]
        
    pipe.vae_scale_factor = 2 ** (len(pipe.vae.config.block_out_channels) - 1)

    height=width=pipe.unet.config.sample_size * pipe.vae_scale_factor
    num_channels_latents = pipe.unet.config.in_channels
    num_images_per_prompt = n
    
    
    all_latents = []
    for gen in generators:
        shape = (
                    # batch_size * num_images_per_prompt,
                    num_images_per_prompt,
                    num_channels_latents,
                    height // pipe.vae_scale_factor,
                    width // pipe.vae_scale_factor,
                )

        latents = randn_tensor(shape, generator=gen, device=device,dtype=dtype)
        latents = latents * pipe.scheduler.init_noise_sigma
        all_latents.append(latents)
        # latents.to(embd_dtype)
    latents = torch.cat(all_latents, 0)
    return latents